# Write a Shell Script check whether the entered character is Vowel or consonant


echo "Enter a character"
read ch

case $ch in
a)
echo "Vowel entered"
;;
e)
echo "Vowel entered"
;;
i)
echo "Vowel entered"
;;
o)
echo "Vowel entered"
;;
u)
echo "Vowel entered"
;;
*)
echo "Consonant entered"
;;
esac
